    private void printInvoice(String iid) {
        try {
            String path = SystemVariableList.REPORT_BASE_URL + "invoice.jasper";
//            InputStream inputStream = new FileInputStream(path);
            HashMap params = new HashMap();
            params.put("iid", iid);

            JasperPrint fillReport = JasperFillManager.fillReport(path, params, MySQL.getConnection());
            JasperViewer.viewReport(fillReport, false);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void updateTotal() {
        double total = 0;

        for (int i = 0; i < jTable1.getRowCount(); i++) {
            String t = jTable1.getValueAt(i, 7).toString();
            total = total + Double.parseDouble(t);
        }

        jLabel19.setText(df.format(total));
    }
