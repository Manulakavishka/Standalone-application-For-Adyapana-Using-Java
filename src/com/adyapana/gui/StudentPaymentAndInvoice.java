    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
//        String cid = jLabel2.getText();
        String sid = l_id.getText();
        String fname = l_fname.getText();
        String lname = l_lname.getText();
        String teacher_name = jComboBox2.getSelectedItem().toString().split("_")[1];
        String teacher_nic = jComboBox2.getSelectedItem().toString().split("_")[0];
        String subject_name = jComboBox3.getSelectedItem().toString().split("_")[1];
        String month_name = jComboBox5.getSelectedItem().toString().split("_")[1];
        String value = jLabel2.getText();

        if (sid.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please select Student", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM student WHERE student.id = '" + sid + "'");
                rs.next();

                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

                Vector v = new Vector();
                v.add(sid);
                v.add(fname);
                v.add(lname);
                v.add(teacher_nic);
                v.add(teacher_name);
                v.add(subject_name);
                v.add(month_name);
                v.add(value);

                dtm.addRow(v);

                updateTotal();
                JOptionPane.showMessageDialog(this, "Student subject details added to the Invoice", "Success", JOptionPane.INFORMATION_MESSAGE);

                if (jTable1.getRowCount() == 1) {
                    jButton1.setText("Update Student");
                    jButton1.setEnabled(false);
                }

            } catch (Exception e) {
                e.printStackTrace();

            }
        }
        //  
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please select a Invoice item", "warning", JOptionPane.WARNING_MESSAGE);
            } else {

                int option = JOptionPane.showConfirmDialog(this, "Do you want to remove this item?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

                if (option == JOptionPane.YES_OPTION) {

                    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

                    dtm.removeRow(r);

                    jLabel19.setText("0.00");
                    jTextField3.setText("");
                    jTextField3.setEditable(false);
                    jLabel24.setText("0.00");
                    jComboBox1.setSelectedIndex(0);

                    JOptionPane.showMessageDialog(this, "student subject details item removed", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

